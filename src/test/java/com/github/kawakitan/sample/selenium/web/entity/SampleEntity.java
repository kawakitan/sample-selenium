package com.github.kawakitan.sample.selenium.web.entity;

public class SampleEntity {

	public Integer id;

	private String title;

	private int count;

	public Integer getID() {
		return id;
	}

	public void setId(Integer no) {
		this.id = no;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

}
